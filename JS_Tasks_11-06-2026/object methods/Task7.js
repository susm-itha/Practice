//object methods
let num = 7.6;
console.log("Original Number:", num);
console.log("Math.round():", Math.round(num));
console.log("Math.floor():", Math.floor(num));
console.log("Math.ceil():", Math.ceil(num));
console.log("Math.random():", Math.random());

//length, slice, substring, replace
let str = "  JavaScript Programming  ";
console.log("Length:", str.length);
console.log("Slice:", str.slice(2, 12));
console.log("Substring:", str.substring(2, 12));
console.log("Replace:", str.replace("Programming", "Language"));
console.log("Uppercase:", str.toUpperCase());
console.log("Lowercase:", str.toLowerCase());


