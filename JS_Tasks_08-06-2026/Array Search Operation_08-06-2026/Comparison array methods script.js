let arr = [10, 20, 30]

// push()
arr.push(40)
console.log(arr)

// unshift()
arr.unshift(5)
console.log(arr)

// splice() add
arr.splice(2, 0, 15)
console.log(arr)

// pop()
arr.pop()
console.log(arr)

// shift()
arr.shift()
console.log(arr)

// splice() remove
arr.splice(1, 1)
console.log(arr)

// includes()
console.log(arr.includes(20))

// indexOf()
console.log(arr.indexOf(20))