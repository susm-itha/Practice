//Add One Element Using unshift()
let fruits = ["Banana", "Mango"]
fruits.unshift("Apple")
console.log(fruits)

//Add Multiple Elements Using unshift()
let numbers = [3, 4, 5]
numbers.unshift(1, 2)
console.log(numbers)

//Difference Between push() and unshift()
let arr = [2, 3]
arr.push(4)
console.log(arr)
arr.unshift(1)
console.log(arr)