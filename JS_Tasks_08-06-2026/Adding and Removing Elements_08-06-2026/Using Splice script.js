//Replace Elements Using splice()
let animals = ["Cat", "Dog", "Lion"]
animals.splice(1, 1, "Tiger")
console.log(animals)

//Remove and Add Elements Together
let data = [10, 20, 50]
data.splice(2, 0, 30, 40)
console.log(data)

//Using splice() in a Student Array
let students = ["manasu", "teju"]
students.splice(1, 0, "Susmitha")
console.log(students)