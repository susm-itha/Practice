//Add One Element Using push()
let fruits = ["Apple", "Banana"]
fruits.push("Mango")
console.log(fruits)

//Add Multiple Elements Using push()
let numbers = [1, 2]
numbers.push(3, 4, 5)
console.log(numbers)

//Store the Returned Length
let colors = ["Red", "Blue"]
let newLength = colors.push("Green")
console.log(colors)
console.log("New Length:", newLength)