//Remove Elements Using pop()
let fruits = ["Apple", "Banana", "Mango"]
fruits.pop()
console.log(fruits)

//Remove Elements Using shift()
let students = ["susmitha", "manasu", "teju"]
students.shift()
console.log(students)

//Remove Elements Using splice()
let animals = ["Cat", "Dog", "Lion"]
animals.splice(1, 1)
console.log(animals)