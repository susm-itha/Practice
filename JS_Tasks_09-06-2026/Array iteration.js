//using for loop
const fruit = ["Apple", "Banana", "Mango", "Orange"]
for (let i = 0; i < fruit.length; i++) {
    console.log(fruit[i])
}

//using while loop
const fruitss = ["Apple", "Banana", "Mango", "Orange"]
let i = 0
while (i < fruitss.length) {
    console.log(fruitss[i])
    i++
}

//using for..of
const fruitts = ["Apple", "Banana", "Mango", "Orange"]
for (const fruit of fruitts) {
    console.log(fruit)
}

//using foreach()
const fruiit = ["Apple", "Banana", "Mango", "Orange"]
fruiit.forEach(function(fruiit) {
    console.log(fruiit)
})

