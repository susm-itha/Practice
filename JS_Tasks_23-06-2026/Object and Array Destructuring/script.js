//Object Destructuring
const student = {
  name: "Susmitha",
  age: 21,
  course: "B.Tech"
};
// Destructuring
const { name, age, course } = student;
console.log(name);   
console.log(age);    
console.log(course); 


//Array Destructuring
const colors = ["Red", "Green", "Blue"];
// Destructuring
const [first, second, third] = colors;
console.log(first);  
console.log(second); 
console.log(third);  

